package object;

public class MyMath {
    long add(long a, long b) {// 메소드명이 동일해선 안된다
        return a + b;
    }

    long substract(long a, long b) {
        return a - b;
    }

    long multiply(long a, long b) {
        return a * b;
    }

    double divide(long a, long b) {
        return a / b;
    }
}
